#include <bits/stdc++.h>
using namespace std;
int main() {
   int l = 1, r = 1e9;
   while (l <= r) {
      int mid = (l + r) / 2;
      cout << "? " << mid << endl;
      char c;
      cin >> c;
      if (c == '=') {
         cout << "! " << mid << endl;
         break;
      }
      else if (c == '>') {
         l = mid + 1;
      }
      else if (c == '<') {
         r = mid - 1;
      }
   }
}